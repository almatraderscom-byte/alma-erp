const MISSED_CLOSERS = [
  'কাযা নামাজ এখনই পড়ুন, Sir। আল্লাহ অত্যন্ত ক্ষমাশীল ও দয়ালু।\n\nপড়েছেন কি?',
  'তওবা করুন, কাযা আদায় করুন — আল্লাহ মাফ করবেন, Sir।\n\nপড়েছেন কি?',
  'ওয়াক্ত ফুরিয়েছে — কাযায় পড়লেও সওয়াব আছে, Sir।\n\nপড়েছেন কি?',
]

function buildMessage({ openers, bodies, closers, waqtName, seed }) {
  const opener = pick(openers, seed + ':open').replace(/\{name\}/g, waqtName)
  const body = pick(bodies, seed + ':body')
  const closer = closers ? pick(closers, seed + ':close') : ''
  return closer ? `${opener}\n\n${body}\n\n${closer}` : `${opener}\n\n${body}`
}

/**
 * @param {{ tier: 1|2|3|'missed', waqt: string, waqtName: string, dateYmd: string, remindersSent?: number, griefContext?: string }} opts
 */
export function salahReminderMessage({
  tier,
  waqt,
  waqtName,
  dateYmd,
  remindersSent = 0,
  griefContext = '',
  /** 0=telegram, 1=ntfy, 2=voice — different copy per channel at same step */
  variant = 0,
}) {
  const seed = `${dateYmd}:${waqt}:t${tier}:r${remindersSent}:v${variant}`

  if (tier === 'missed') {
    let msg = buildMessage({
      openers: MISSED_OPEN,
      bodies: MISSED_BODY,
      closers: MISSED_CLOSERS,
      waqtName,
      seed,
    })
    if (griefContext?.trim()) {
      msg += `\n\n${griefContext.trim()}`
    }
    return msg
  }

  const tierNum = tier
  const openers = tierNum === 1 ? LEVEL1_OPEN : tierNum === 2 ? LEVEL2_OPEN : LEVEL3_OPEN
  const bodies = tierNum === 1 ? LEVEL1_BODY : tierNum === 2 ? LEVEL2_BODY : LEVEL3_BODY
  const closers = LEVEL_CLOSERS[tierNum]

  let msg = buildMessage({ openers, bodies, closers, waqtName, seed })

  if (tierNum === 3 && griefContext?.trim()) {
    msg += `\n\n${griefContext.trim()}\n\nতার কথা মনে রাখুন — তিনিও কি জানতেন শেষ নামাজটা শেষ নামাজ হয়ে যাবে?`
  }

  return msg
}

/** Per-channel copy for one escalation step (no duplicate hadith across pushes). */
export function salahChannelMessages(opts) {
  const base = { ...opts, griefContext: opts.griefContext ?? '' }
  const telegram = salahReminderMessage({ ...base, variant: 0 })
  const ntfy = salahReminderMessage({ ...base, variant: 1 })
  const voice = salahReminderMessage({ ...base, variant: 2 })
  const voiceShort = voice.split('\n\n').slice(0, 2).join('\n\n').slice(0, 380)
  return { telegram, ntfy, voice: voiceShort }
}

