/**
 * Rotating salah reminder copy — hadith, Quran, akhirah.
 * Same seed (date + waqt + tier + reminder count) → same line that day;
 * different waqts / days / escalation steps → different lines.
 */

const WAQT_INDEX = { fajr: 0, dhuhr: 1, asr: 2, maghrib: 3, isha: 4 }

function pickIndex(pool, seed) {
  let h = 0
  for (let i = 0; i < seed.length; i++) h = ((h << 5) - h + seed.charCodeAt(i)) | 0
  return Math.abs(h) % pool.length
}

function pick(pool, seed) {
  return pool[pickIndex(pool, seed)]
}

